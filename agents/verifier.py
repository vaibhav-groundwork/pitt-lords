# Public entry point
# ---------------------------------------------------------------------------

def run_verifier(lease_id: str) -> None:
    """Run verification on all LLM-judged compliance findings for a lease.

    Reads compliance_findings (populated by agents/compliance_diff.py) and
    checks each verdict for internal consistency against the statutory text and
    lease clause using gpt-4o-mini. Writes results back to verifier_confirmed
    and verifier_note. Idempotent: verifier fields are reset to NULL before any
    new results are written.

    Parameters
    ----------
    lease_id:
        UUID string of a lease that has already been processed by
        agents/compliance_diff.py.

    Raises
    ------
    RuntimeError
        If settings.openai_api_key is not set, raised immediately rather than
        letting every batch fail with a less obvious error.
    """
    if not settings.openai_api_key:
        raise RuntimeError(
            "settings.openai_api_key is not set. "
            "Set the OPENAI_API_KEY environment variable (or add it to .env) "
            "before running the verifier."
        )

    # LiteLLM reads the key from the environment; set it from settings so the
    # rest of the codebase's single source of truth (api/config.py) is respected.
    import os
    os.environ["OPENAI_API_KEY"] = settings.openai_api_key

    print(f"Running verifier for lease {lease_id} ...")

    conn = get_connection()

    try:
        rows = get_findings_to_verify(conn, lease_id)

        if not rows:
            print(
                "No LLM-judged findings to verify for this lease "
                "(all findings may be 'absent', or compliance_diff has not been run yet)."
            )
            return

        print(f"Found {len(rows)} findings to verify.")

        # Reset verifier fields before writing new results so stale notes from
        # a prior compliance_diff run cannot appear to apply to a new verdict.
        _reset_verifier_fields(conn, lease_id)

        print(f"\nRunning verification in batches of {_BATCH_SIZE} ...")
        verifications = classify_verification(rows)

        _update_verifier_fields(conn, lease_id, verifications)

        # Build summary.
        total_sent = len(rows)
        total_verified = len(verifications)
        failed_count = total_sent - total_verified

        confirmed_entries = [v for v in verifications if v["confirmed"]]
        disputed_entries = [v for v in verifications if not v["confirmed"]]

        # Collect keys that were sent but got no validated result back.
        verified_keys = {v["requirement_key"] for v in verifications}
        all_sent_keys = {row["requirement_key"] for row in rows}
        failed_keys = sorted(all_sent_keys - verified_keys)

        print(f"\n{'=' * 60}")
        print(f"Verifier summary for lease {lease_id}:")
        print(f"  Total findings sent for verification: {total_sent}")
        print(f"  Successfully verified:                {total_verified}")
        print(f"  Confirmed (reasoning holds):          {len(confirmed_entries)}")
        print(f"  Not confirmed (disagreements):        {len(disputed_entries)}")
        if disputed_entries:
            print("  Disputed findings (most actionable):")
            for v in sorted(disputed_entries, key=lambda x: x["requirement_key"]):
                print(f"    {v['requirement_key']}: {v['note']}")
        if failed_count:
            print(f"  Failed to verify (API/parse error):  {failed_count}")
            if failed_keys:
                print(f"    Failed keys: {', '.join(failed_keys)}")
        print(f"{'=' * 60}")

    finally:
        conn.close()


# ---------------------------------------------------------------------------
# CLI entry point
# ---------------------------------------------------------------------------

if __name__ == "__main__":
    if len(sys.argv) != 2:
        print("Usage: python agents/verifier.py <lease_id>")
        sys.exit(1)

    run_verifier(sys.argv[1])
