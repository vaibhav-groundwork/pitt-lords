"""
Verifier Agent -- reasoning-check on compliance-diff output, not a re-judgment.

This agent is Phase 5 in the pipeline. It reads compliance_findings (populated
by agents/compliance_diff.py) and checks whether each verdict is internally
consistent with the evidence that was shown to the compliance-diff agent: the
full statutory text and the matched lease clause.

What "verify" means precisely
-------------------------------
The verifier does NOT independently re-derive a compliance verdict from scratch.
That would just be a second diff agent making a second judgment call, which adds
cost and latency without adding a meaningful check. Instead, given the actual
statutory full_text and the actual matched lease clause_text side by side, it
checks whether the compliance-diff agent's stated status and explanation
genuinely follow from comparing those two texts. This is a narrower, more
mechanical reasoning-check: is the stated conclusion consistent with the stated
evidence? It does not require the verifier to know the law independently.

Why a different model family is required
-----------------------------------------
Using the same model to verify its own output would not catch correlated
reasoning errors -- systematic blind spots and failure modes shared across
calls to the same model. This agent deliberately uses gpt-4o-mini via LiteLLM
rather than Claude (used by agents/lease_parser.py and
agents/compliance_diff.py), so that a judgment error in the compliance-diff
step has a meaningful chance of being caught rather than being repeated. This
architectural decision is logged in docs/decisions.md.

This file must never import the anthropic SDK. Any change that adds an anthropic
import here defeats the purpose of having a separate verifier model.

Why 'absent' findings are excluded
------------------------------------
compliance_findings rows with status='absent' are generated by a deterministic,
rule-based path in compliance_diff.py with no LLM judgment behind them: if
lease_clauses shows no matching clause, the status is absent, period. There is
no LLM reasoning to check for consistency. Only LLM-judged statuses
('compliant', 'contradicts', 'needs_review') are sent to this agent.

LiteLLM tool-calling shape (differs from Anthropic SDK used elsewhere)
------------------------------------------------------------------------
LiteLLM uses the OpenAI function-calling wrapper shape, not Anthropic's shape:

  tools = [{"type": "function", "function": {"name": ..., "parameters": ...}}]
  tool_choice = {"type": "function", "function": {"name": ...}}

Results are at response.choices[0].message.tool_calls (a list, possibly None).
Each call's arguments are a raw JSON STRING at tool_calls[0].function.arguments
that must be json.loads()'d explicitly. This is structurally different from
Anthropic's tool_block.input (a pre-parsed dict). json.loads() failures are
treated as batch failures, same as any other error.
"""

import json
import sys
from typing import Any

import litellm

from api.config import settings
from api.db import get_connection

# Smaller than the other two agents' _BATCH_SIZE=25. Each row here carries
# three pieces of text (full_text, clause_text, and the original explanation),
# so batches need to stay a manageable prompt size.
_BATCH_SIZE = 15


# ---------------------------------------------------------------------------
# Types
# ---------------------------------------------------------------------------

FindingRow = dict[str, Any]      # one row from the join query in get_findings_to_verify
Verification = dict[str, Any]    # one validated entry from the LLM response


# ---------------------------------------------------------------------------
# Tool definition (OpenAI function-calling shape, not Anthropic shape)
# ---------------------------------------------------------------------------

