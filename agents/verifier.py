# Database helpers
# ---------------------------------------------------------------------------

def get_findings_to_verify(conn, lease_id: str) -> list[FindingRow]:
    """Query compliance findings that have LLM-judged statuses for this lease.

    Joins compliance_findings to legal_sources (for full_text and citation) and
    to lease_clauses (for clause_text). Only rows with status IN ('compliant',
    'contradicts', 'needs_review') are returned -- 'absent' rows are excluded
    because they carry no LLM reasoning to check.

    Parameters
    ----------
    conn:
        An open psycopg connection obtained via ``get_connection()``.
    lease_id:
        UUID string of the lease whose findings are to be verified.

    Returns
    -------
    list[FindingRow]
        Each dict contains ``requirement_key``, ``citation``, ``full_text``,
        ``clause_text``, ``status``, and ``explanation``.
    """
    with conn.cursor() as cur:
        cur.execute(
            """
            SELECT
                cf.requirement_key,
                ls.citation,
                ls.full_text,
                lc.clause_text,
                cf.status,
                cf.explanation
            FROM compliance_findings cf
            JOIN legal_sources ls
                ON ls.requirement_key = cf.requirement_key
            JOIN lease_clauses lc
                ON lc.requirement_key = cf.requirement_key
                AND lc.lease_id = cf.lease_id
            WHERE cf.lease_id = %(lease_id)s
              AND cf.status IN ('compliant', 'contradicts', 'needs_review')
            ORDER BY cf.requirement_key
            """,
            {"lease_id": lease_id},
        )
        rows = cur.fetchall()
        columns = [desc[0] for desc in cur.description]

    return [dict(zip(columns, row)) for row in rows]


def _reset_verifier_fields(conn, lease_id: str) -> None:
    """Set verifier_confirmed and verifier_note to NULL for all findings in this lease.

    Called before writing new verification results. If compliance_diff was rerun
    and a requirement_key's status changed, a stale verifier_note from a prior
    status must not linger and appear to apply to the new verdict.

    Parameters
    ----------
    conn:
        An open psycopg connection with ``autocommit=True``.
    lease_id:
        UUID string identifying the lease being re-verified.
    """
    with conn.cursor() as cur:
        cur.execute(
            """
            UPDATE compliance_findings
            SET verifier_confirmed = NULL,
                verifier_note = NULL
            WHERE lease_id = %s
            """,
            (lease_id,),
        )


def _update_verifier_fields(
    conn,
    lease_id: str,
    verifications: list[Verification],
) -> None:
    """Write verifier_confirmed and verifier_note back to compliance_findings.

    Only rows that passed validation in ``classify_verification`` are updated.
    Rows that failed API calls or validation remain at NULL (set by
    ``_reset_verifier_fields``), which is the correct signal that verification
    was attempted but did not produce a usable result.

    Parameters
    ----------
    conn:
        An open psycopg connection with ``autocommit=True``.
    lease_id:
        UUID string identifying the lease.
    verifications:
        Validated entries from ``classify_verification``, each containing
        ``requirement_key``, ``confirmed``, and ``note``.
    """
    if not verifications:
        return

    with conn.cursor() as cur:
        cur.executemany(
            """
            UPDATE compliance_findings
            SET verifier_confirmed = %s,
                verifier_note = %s
            WHERE lease_id = %s
              AND requirement_key = %s
            """,
            [
                (v["confirmed"], v["note"], lease_id, v["requirement_key"])
                for v in verifications
            ],
        )


# ---------------------------------------------------------------------------
# Validation
# ---------------------------------------------------------------------------

def _validate_verification(
    entry: Any,
    expected_keys: set[str],
    batch_label: str,
) -> Verification | None:
    """Validate one verification entry returned by gpt-4o-mini.

    Checks in order:
      1. Must be a dict.
      2. ``requirement_key`` must be a non-empty string.
      3. ``requirement_key`` must be in ``expected_keys`` (hallucination guard --
         same pattern as lease_parser.py and compliance_diff.py).
      4. ``confirmed`` must be a boolean.
      5. ``note`` must be a non-empty, non-whitespace-only string.

    Parameters
    ----------
    entry:
        Raw object from the parsed verifications array.
    expected_keys:
        Requirement keys sent to this specific batch.
    batch_label:
        Short string like ``"batch 2/4"`` used in warning messages.

    Returns
    -------
    Verification or None
        The validated entry, or None if it must be skipped.
    """
    if not isinstance(entry, dict):
        print(f"  [WARN] {batch_label}: skipping non-dict verification entry: {entry!r}")
        return None

    key = entry.get("requirement_key")
    confirmed = entry.get("confirmed")
    note = entry.get("note")

    if not isinstance(key, str) or not key:
        print(
            f"  [WARN] {batch_label}: skipping entry with missing/invalid "
            f"requirement_key: {entry!r}"
        )
        return None

    if key not in expected_keys:
        print(
            f"  [WARN] {batch_label}: skipping '{key}' -- not in the set of "
            "requirement_keys sent to the model for this batch "
            "(hallucinated or mistyped key)."
        )
        return None

    if not isinstance(confirmed, bool):
        print(
            f"  [WARN] {batch_label}: skipping '{key}' -- 'confirmed' must be "
            f"boolean, got {confirmed!r}"
        )
        return None

    if not isinstance(note, str) or not note.strip():
        print(
            f"  [WARN] {batch_label}: skipping '{key}' -- 'note' is missing, "
            "non-string, or whitespace-only."
        )
        return None

    return {
        "requirement_key": key,
        "confirmed": confirmed,
        "note": note,
    }


