def classify_verification(rows_to_verify: list[FindingRow]) -> list[Verification]:
    """Check compliance findings for internal consistency using gpt-4o-mini.

    Each batch prompt presents the statutory text, the matched lease clause, and
    the compliance-diff agent's stated verdict side by side, framed as a
    reasoning-consistency check rather than an independent legal judgment. The
    model is asked whether the stated conclusion genuinely follows from comparing
    the two texts, not whether it would have reached the same conclusion itself.

    On any failure (API exception, missing/empty tool_calls, JSON parse failure),
    the affected requirement_keys are logged by name and their
    verifier_confirmed/verifier_note remain NULL in the database (already reset
    by ``_reset_verifier_fields``). Failures do not raise -- other batches
    continue.

    Parameters
    ----------
    rows_to_verify:
        Rows from ``get_findings_to_verify``. Each must contain
        ``requirement_key``, ``citation``, ``full_text``, ``clause_text``,
        ``status``, and ``explanation``.

    Returns
    -------
    list[Verification]
        Validated verification entries. Keys that failed or were omitted by the
        model are not included -- their database rows remain at NULL, which is
        the correct representation of "attempted but unresolved."
    """
    all_keys = [row["requirement_key"] for row in rows_to_verify]
    rows_by_key = {row["requirement_key"]: row for row in rows_to_verify}

    batches = [
        all_keys[i : i + _BATCH_SIZE] for i in range(0, len(all_keys), _BATCH_SIZE)
    ]
    total_batches = len(batches)
    all_results: list[Verification] = []

    for batch_idx, batch_keys in enumerate(batches, start=1):
        batch_label = f"batch {batch_idx}/{total_batches}"
        expected_keys = set(batch_keys)

        sections: list[str] = []
        for key in batch_keys:
            row = rows_by_key[key]
            sections.append(
                f"REQUIREMENT: {key}\n"
                f"Citation: {row['citation']}\n"
                f"Full statutory text:\n{row['full_text']}\n\n"
                f"Matched lease clause:\n{row['clause_text']}\n\n"
                f"Compliance-diff verdict: {row['status']}\n"
                f"Compliance-diff explanation: {row['explanation']}"
            )

        prompt = (
            "You are checking the reasoning of an automated compliance review tool.\n\n"
            "For each REQUIREMENT below, you are given:\n"
            "  1. The full statutory text of the legal requirement\n"
            "  2. The matched clause from the lease\n"
            "  3. The compliance-diff agent's verdict (compliant / contradicts / "
            "needs_review) and its explanation\n\n"
            "Your task: check whether the compliance-diff agent's STATED VERDICT is "
            "well-supported by ITS OWN STATED REASONING when you compare the "
            "statutory text to the lease clause. Think of yourself as a supervisor "
            "checking this agent's work: would you sign off that its conclusion "
            "logically follows from the comparison it was asked to make?\n\n"
            "CRITICAL DISTINCTION -- read this carefully:\n"
            "confirmed=true means: the agent's reasoning process was sound, given "
            "the two texts. This says nothing about whether the underlying lease has "
            "a legal problem. A 'contradicts' verdict about a real legal problem "
            "should be confirmed=true if the reasoning that led to it was correct.\n"
            "confirmed=false means specifically: the agent's OWN CONCLUSION does NOT "
            "follow from its OWN STATED COMPARISON -- for example, it said 'compliant' "
            "but its own explanation describes a conflict; or it said 'contradicts' "
            "but the clause it quoted does not actually conflict with the statute; or "
            "its explanation does not logically connect to the verdict it chose.\n"
            "Do NOT set confirmed=false merely because the underlying finding "
            "describes a real legal problem in the lease. A correctly-reasoned "
            "'contradicts' or 'needs_review' about a genuine issue should be "
            "confirmed=true.\n\n"
            "Concrete example:\n"
            "  - The diff agent says 'contradicts' and correctly explains that the "
            "lease clause conflicts with the statute on late fees. That is "
            "confirmed=true, even though it means the lease has a problem.\n"
            "  - The diff agent says 'compliant' but its own explanation actually "
            "describes a conflict between the clause and the statute. That is "
            "confirmed=false, because the agent's own reasoning undermines its "
            "own verdict.\n\n"
            "In your note: write as if speaking directly to the landlord -- no "
            "references to 'the agent', 'compliance-diff', or any internal "
            "component. Do not quote the status label as a technical term; "
            "describe what is happening in plain language. Do not narrate your "
            "reasoning process; state the substantive legal point directly. "
            "If confirmed=true: briefly confirm what the law requires and why the "
            "lease's handling of it holds up. "
            "If confirmed=false: explain the actual legal conflict or gap in the "
            "landlord's own terms -- what the law requires, what the lease "
            "actually says or does, and why it matters.\n\n"
            + "\n\n---\n\n".join(sections)
            + "\n\nCall record_verifications with one entry per REQUIREMENT listed above."
        )

        print(f"  [{batch_label}] Verifying {len(batch_keys)} findings ...")

        try:
            response = litellm.completion(
                model="gpt-4o-mini",
                max_tokens=4096,
                tools=_RECORD_VERIFICATIONS_TOOL,
                tool_choice=_TOOL_CHOICE,
                messages=[{"role": "user", "content": prompt}],
            )
        except Exception as exc:
            failed_keys_str = ", ".join(sorted(batch_keys))
            print(
                f"  [ERROR] {batch_label}: LiteLLM completion failed -- {exc}\n"
                f"  Failed requirement_keys: {failed_keys_str}"
            )
            # Verifier fields remain NULL (already reset). Do not append
            # placeholder entries here -- NULL is the correct representation
            # of "attempted but unresolved" for this agent.
            continue

        # Extract tool_calls from the OpenAI-shaped response.
        tool_calls = None
        try:
            tool_calls = response.choices[0].message.tool_calls
        except (AttributeError, IndexError):
            pass

        if not tool_calls:
            failed_keys_str = ", ".join(sorted(batch_keys))
            print(
                f"  [ERROR] {batch_label}: no tool_calls in response.\n"
                f"  Failed requirement_keys: {failed_keys_str}"
            )
            continue

        # Arguments are a raw JSON string -- must be parsed explicitly.
        try:
            arguments = json.loads(tool_calls[0].function.arguments)
        except (json.JSONDecodeError, AttributeError, IndexError) as exc:
            failed_keys_str = ", ".join(sorted(batch_keys))
            print(
                f"  [ERROR] {batch_label}: failed to parse tool_calls arguments "
                f"as JSON -- {exc}\n"
                f"  Failed requirement_keys: {failed_keys_str}"
            )
            continue

        raw_entries: list[Any] = arguments.get("verifications", [])

        validated: list[Verification] = []
        for entry in raw_entries:
            result = _validate_verification(entry, expected_keys, batch_label)
            if result is not None:
                validated.append(result)

        # Warn about keys the model omitted.
        returned_keys = {v["requirement_key"] for v in validated}
        missing_keys = expected_keys - returned_keys
        if missing_keys:
            print(
                f"  [WARN] {batch_label}: model did not return verifications for: "
                + ", ".join(sorted(missing_keys))
            )

        all_results.extend(validated)

    return all_results


